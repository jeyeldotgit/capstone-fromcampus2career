type SeedIdMap<TCodes extends readonly string[]> = {
  readonly [Code in TCodes[number]]: string;
};

function formatUuid(namespace: string, index: number): string {
  return `00000000-0000-${namespace}-0000-${index.toString().padStart(12, "0")}`;
}

function buildIdMap<TCodes extends readonly string[]>(
  namespace: string,
  codes: TCodes,
): SeedIdMap<TCodes> {
  return Object.freeze(
    Object.fromEntries(codes.map((code, index) => [code, formatUuid(namespace, index + 1)])),
  ) as SeedIdMap<TCodes>;
}

