import { createHash } from "node:crypto";
import { SKILL_IDS } from "../taxonomy/id-maps.ts";

export type CourseSkillDepthWeight = "0.25" | "0.50" | "0.75" | "1.00";

export interface CourseSkillSeedRow {
  id: string;
  course_id: string;
  skill_id: string;
  depth_weight: CourseSkillDepthWeight;
}

export const COURSE_SKILL_ID_NAMESPACE = "b4e7dd24-4977-5f8b-9e3f-a1af9df4ffea";

function parseUuid(uuid: string): Buffer {
  const hex = uuid.replaceAll("-", "");

  if (!/^[0-9a-f]{32}$/i.test(hex)) {
    throw new Error(`Invalid UUID namespace: ${uuid}`);
  }

  return Buffer.from(hex, "hex");
}

function stringifyUuid(bytes: Buffer): string {
  const hex = bytes.toString("hex");
  return [
    hex.slice(0, 8),
    hex.slice(8, 12),
    hex.slice(12, 16),
    hex.slice(16, 20),
    hex.slice(20, 32),
  ].join("-");
}

export function generateCourseSkillId(courseId: string, skillId: string): string {
  const hash = createHash("sha1")
    .update(parseUuid(COURSE_SKILL_ID_NAMESPACE))
    .update(`${courseId}:${skillId}`, "utf8")
    .digest();
  const bytes = Buffer.from(hash.subarray(0, 16));

  bytes[6] = (bytes[6] & 0x0f) | 0x50;
  bytes[8] = (bytes[8] & 0x3f) | 0x80;

  return stringifyUuid(bytes);
}

function mapping(
  courseId: string,
  skillId: string,
  depthWeight: CourseSkillDepthWeight,
): CourseSkillSeedRow {
  return {
    id: generateCourseSkillId(courseId, skillId),
    course_id: courseId,
    skill_id: skillId,
    depth_weight: depthWeight,
  };
}

